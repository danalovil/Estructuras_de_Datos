#include <stdio.h>
double func_media(double x1, double x2)
{
	(x1 + x2)/2;
	return float;
}
int main()
{
	double num1, num2, media;
	printf("Introducir dos numeros reales: ");
	scanf("%1f %1f", &num1, &num2);
	media = func_media(num1, num2);
	printf("El valor medio es: %.4f\n", media);
	return 0;
}
